  Application Name: MedianFilterFlows                                                                                                                       
                                                                                             
  Summary:  
  Scan data from file in resources is retrieved and after applying a median filter, 
  viewed as a point cloud. This sample uses the flow approach. 
  
  Description:                                                                                                                    
  This sample creates a ScanFileDriver and starts playing of scans from the 
  test scandata file. The filtered scan is shown in the PointCloud viewer on 
  the webpage.
  
  How to run: 
  Scans can be viewed in the PointCloud viewer on the webpage.
  After some time the playing stops and the last scan is still shown. To replay, the 
  sample must be restarted.
    
